#include<unistd.h>

void	ft_print_comb2(void)
{
	int		nb;
	int		nb2;
	char	c;

	nb = -1;
	while (++nb <= 98)
	{
		nb2 = nb;
		while (++nb2 <= 99)
		{
			c = (nb / 10) + 48;
			write(1, &c, 1);
			c = (nb % 10) + 48;
			write(1, &c, 1);
			write(1, " ", 1);
			c = (nb2 / 10) + 48;
			write(1, &c, 1);
			c = (nb2 % 10) + 48;
			write(1, &c, 1);
			if (nb != 98)
			{
				write(1, ", ", 2);
			}
		}
	}	
}
